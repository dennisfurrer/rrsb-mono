#!/usr/bin/env node
/**
 * Per-app semantic version bumper, run by .github/workflows/version.yml on every
 * push to main. For each versioned package whose files changed in the push, it
 * bumps the patch number in package.json. Frontends additionally get a generated
 * src/version.ts so the UI can read its own version (e.g. to gate features).
 *
 * Patch-only on purpose: it must work no matter how commits are worded. To cut a
 * minor/major release, edit the package.json version by hand in a normal commit;
 * the next change will then patch-bump from there.
 */
import { execSync } from "node:child_process";
import { readFileSync, writeFileSync, existsSync } from "node:fs";

// dir = package path; frontend = also emit src/version.ts
const PACKAGES = [
  { dir: "apps/scoreboard-ui", frontend: true },
  { dir: "apps/statistics-ui", frontend: true },
  { dir: "apps/admin-ui", frontend: true },
  { dir: "apps/scores-and-stats-api", frontend: false },
  { dir: "packages/db", frontend: false },
];

function sh(cmd) {
  return execSync(cmd, { encoding: "utf8" }).trim();
}
function commitExists(sha) {
  try {
    sh(`git cat-file -e ${sha}^{commit}`);
    return true;
  } catch {
    return false;
  }
}

const after = process.env.AFTER || "HEAD";
const before = process.env.BEFORE || "";
const isZero = /^0+$/.test(before);

// Range of the push. Fall back to the single parent commit on first push / new branch.
let range;
if (before && !isZero && commitExists(before)) {
  range = `${before} ${after}`;
} else {
  range = `${after}~1 ${after}`;
}

let changed = [];
try {
  changed = sh(`git diff --name-only ${range}`).split("\n").filter(Boolean);
} catch {
  changed = [];
}

function bumpPatch(v) {
  const m = /^(\d+)\.(\d+)\.(\d+)(.*)$/.exec(v || "0.0.0");
  if (!m) return "0.0.1";
  return `${m[1]}.${m[2]}.${Number(m[3]) + 1}${m[4] || ""}`;
}

const bumped = [];
for (const pkg of PACKAGES) {
  if (!changed.some((f) => f.startsWith(pkg.dir + "/"))) continue;
  const pjPath = `${pkg.dir}/package.json`;
  if (!existsSync(pjPath)) continue;

  const pj = JSON.parse(readFileSync(pjPath, "utf8"));
  const next = bumpPatch(pj.version);
  pj.version = next;
  writeFileSync(pjPath, JSON.stringify(pj, null, 2) + "\n");

  if (pkg.frontend) {
    // Vite apps use src/, Next.js apps use lib/.
    const base = existsSync(`${pkg.dir}/src`) ? "src" : existsSync(`${pkg.dir}/lib`) ? "lib" : null;
    if (base) {
      writeFileSync(
        `${pkg.dir}/${base}/version.ts`,
        `// Auto-generated by .github/workflows/version.yml — do not edit.\nexport const VERSION = "${next}";\n`
      );
    }
  }
  bumped.push(`${pkg.dir} -> ${next}`);
}

if (bumped.length) {
  console.log("Bumped:\n  " + bumped.join("\n  "));
} else {
  console.log("No versioned package changed; nothing to bump.");
}
